section .data

rows: db 4 ; беззнаковые числа
cols: db 3 

align 8 ; отступ для оптимизации, адресс данных должен быть кратен их размеру (8 байт) - тогда считывание за 1 раз происходит

matrix: dq 1, 2, 3    ; 1-я строка
        dq 4, 5, -6    ; 2-я строка
        dq -7, 8, 9    ; 3-я строка
        dq 10,1,-12   ; 4-я строка


temp_min: dq 0x7FFFFFFFFFFFFFFF ; максимальное знаковое число
shift_next: dq 0 ; место для расчета сдвига
temp: dq 0 ; место для временных подсчетов

; Ситуация: У нас матрица 4x3 (4 строки, 3 столбца) в памяти.
; Элемент [0][0] (строка 0, столбец 0) лежит по адресу matrix.
; Где лежит элемент [0][1]? matrix + 8 (потому что мы пропустили один qword).
; Где лежит элемент [1][0] (второй элемент первого столбца)? Это начало второй строки: matrix + (3 * 8) = matrix + 24.


section .bss

align 8
; место, где мы вычислим правильный порядок столбцов и только потом расставим все как надо 
; Без указателей (прямая сортировка):
; Чтобы поменять два ящика местами, тебе нужно:
; Вытащить из первого ящика все вещи (числа) и положить их на пол (в буфер).
; Вытащить из второго ящика все вещи и положить их в первый.
; С пола переложить все вещи во второй ящик.
; Это ОЧЕНЬ много работы. Для столбца из 255 строк это 255 операций копирования, умноженные на 2 (туда-обратно), итого ~500 копирований на одну перестановку.
; Этот подход - Ничего копировать не нужно! Это просто обмен двух 8-байтных чисел (адресов).
ptr_arr: resq 256 ; директива resq обрабатываются на этапе сборки, [rows] - это значение, которое станет известно только во время выполнения программы
min_vals: resq 256 ; массив с минимальными элементами
col_buffer: resq 256

section .text
global _start

_start: 

    ; ---------- Необходимые значения ----------
    xor rcx, rcx
    mov rax, 8
    mov cl, [cols]
    mul rcx ; сдвиг = (8 * cols) байт
    mov [shift_next], rax ; сдвиг для следующего элемента в столбце

    ; Загружаем размеры матрицы - начальная инициализация
    ; ---------- Инициализация ----------
    xor rcx, rcx
    mov cl, [cols]      ; кладем количество стобцов в переменную-счетчик для цикла
    mov rbx, ptr_arr      ; rbx = адрес начала массива указателей (кладем адрес!!!)
    mov rbp, min_vals     ; rbp = адрес начала массива с минимальными эл. в столбцах
    mov rax, matrix       ; rax = адрес первого элемента матрицы (столбец 0)




    call fill_ptrs ; call - позволяет запомнить место, куда надо вернутся после завершения подмодуля в отличии от jmp
    call fill_min_vals
    call sort_col
    call modify_matrix
    ; ---------- Выход ----------
    mov edi, 0
    mov eax, 60
    syscall



; Заполняет ptr_arr адресами столбцов
fill_ptrs:
    push rax
    push rbx
    push rcx ; сохраняем значение rcx для следующих циклов (просто mov cl, [cols] в конце)

.loops:
    mov [rbx], rax     ; положи 8 байт из RAX по тому адресу, который лежит в RBX. Положит адрес столбца
    add rbx, 8   ; получаем следующую ячеку в нашем буфере для следующего столбца
    add rax, 8   ; переходим к следующему элементу в матрице, который задает начало нового столбца
    loop .loops      ; уменьшаем rcx и повторяем, пока rcx != 0

    pop rcx ; восстанавливаем значение счетчика
    pop rbx ; восстанавливаем указатели на начало массивов
    pop rax
    ret ; возвращаемся к общему потоку функций

; .loop — это локальная метка. В NASM метки, начинающиеся с точки, считаются локальными и привязаны к последней глобальной метке.






fill_min_vals:
    ;push rcx ; сохраняем счетчик (мб заново ставить mov cl, [cols] )
    ;push rax
    mov cl, [rows] ; теперь длину столбца храним как счетчик
    dec cl ; rows - 1 интерация
    mov rdx, [rax]        ; прочитать значение из памяти по адресу rax в rdx
    mov [temp_min], rdx   ; записать это значение в temp_min
    ; rax остался неизменным (хранит адрес)

    xor r8, r8
    movzx r9, byte [cols] ; счетчик для внешнего цикла

.loop1:
    cmp r8, r9 ; провекра внешнего счетчика
    jge .done
    push rax
    push rcx
    mov rax, [ptr_arr + r8*8] ; свига по строке
    mov rdx, [rax]        ; прочитать значение из памяти по адресу rax в rdx
    mov [temp_min], rdx   ; записать это значение в temp_min
    jmp .loop2

.loop2:    
    add rax, [shift_next] ; переходим к следующему эл в столбце
    mov rdx, [rax] ; rdx = значение по адресу rax
    ; cmp x, y делает x - y и выставляет флаги. Для знаковых чисел нас интересуют два флага:
    ; SF (Sign Flag) — знак результата (1 если результат отрицательный)
    ; OF (Overflow Flag) — переполнение (1 если было переполнение со знаком)
    cmp rdx, [temp_min] ; сравнить temp_min с rdx
    jge .next    ; если rdx >= temp_min, пропускаем замену (SF = OF)
    mov [temp_min], rdx
    .next:
    loop .loop2 

    inc r8 ; внешний цикл
    mov rdx, [temp_min]
    mov [rbp], rdx ; установили минимум
    add rbp, 8

    pop rcx
    inc rcx ; тк loop .loop1 тоже уменьшает на 1 rcx
    pop rax
    loop .loop1

.done:
    ret


sort_col:
    movzx r12, byte [cols]     ; r12 = количество столбцов
    mov r13, 1                  ; флаг: были ли перестановки - Начинаем с 1, чтобы зайти в цикл


.main_loop:
    cmp r13, 0 ; проверка на то, что были перестановки
    je .done
    xor r13, r13

    ; Четная фаза
    ; Сравниваем пары (0,1), (2,3), (4,5)...
    mov r14, 0 ; r14 = начальный индекс для четной фазы (0)
    call .phase ; Вызываем общую подпрограмму фазы
    
    ; Нечетная фаза
    ; Сравниваем пары (1,2), (3,4), (5,6)...
    mov r14, 1 ; r14 = начальный индекс для нечетной фазы (1)
    call .phase
    
    jmp .main_loop


.phase:
    ; r15 = i+1 (второй элемент пары)
    mov r15, r14
    inc r15             ; r15 = r14 + 1

.phase_loop:
    ; Проверяем, не вышли ли за границы массива
    ; Если i+1 >= cols, то фаза закончена
    cmp r15, r12
    jge .phase_done     ; Если >=, выходим из фазы

    ; Загружаем значения для сравнения
    ; rax = min_vals[i]
    mov rax, [min_vals + r14*8]
    ; rbx = min_vals[i+1]
    mov rbx, [min_vals + r15*8]

    ; Сравниваем: если min_vals[i] <= min_vals[i+1], то порядок правильный
    cmp rax, rbx
    jle .phase_next      ; Если <=, переходим к следующей паре

    ; ===== НУЖНА ПЕРЕСТАНОВКА =====
    ; Меняем местами min_vals[i] и min_vals[i+1]
    ; rdx = min_vals[i]
    mov rdx, [min_vals + r14*8]
    ; rcx = min_vals[i+1]
    mov rcx, [min_vals + r15*8]
    ; min_vals[i] = rcx (бывший i+1)
    mov [min_vals + r14*8], rcx
    ; min_vals[i+1] = rdx (бывший i)
    mov [min_vals + r15*8], rdx

    ; Меняем местами ptr_arr[i] и ptr_arr[i+1] (СИНХРОННО!)
    ; rdx = ptr_arr[i]
    mov rdx, [ptr_arr + r14*8]
    ; rcx = ptr_arr[i+1]
    mov rcx, [ptr_arr + r15*8]
    ; ptr_arr[i] = rcx (указатель на столбец, который теперь должен быть на позиции i)
    mov [ptr_arr + r14*8], rcx
    ; ptr_arr[i+1] = rdx (указатель на столбец, который теперь должен быть на позиции i+1)
    mov [ptr_arr + r15*8], rdx

    ; Устанавливаем флаг, что были перестановки
    mov r13, 1

.phase_next:
    ; Переходим к следующей паре
    ; Для четной фазы: i было 0, станет 2, потом 4...
    ; Для нечетной фазы: i было 1, станет 3, потом 5...
    add r14, 2          ; i += 2
    add r15, 2          ; i+1 += 2
    jmp .phase_loop     ; Повторяем для следующей пары
    
.phase_done:
    ret                 ; Возвращаемся из подпрограммы фазы

.done:
    ret




; =============================================
; modify_matrix - переставляет столбцы матрицы
; Вход: ptr_arr отсортирован (содержит адреса столбцов в нужном порядке)
;       matrix - исходная матрица
; Выход: matrix с переставленными столбцами
; =============================================
modify_matrix:
    ; Загружаем размеры
    movzx r12, byte [cols]        ; r12 = количество столбцов
    movzx r13, byte [rows]        ; r13 = количество строк
    
    ; Буфер для временного хранения столбца
    mov r14, col_buffer            ; r14 = адрес буфера
    
    ; Основной цикл по всем индексам i = 0..cols-1
    xor r15, r15                   ; r15 = i (текущий индекс)
    
.main_loop:
    cmp r15, r12                   ; Все столбцы обработали?
    jge .done                      ; Если да, выходим
    
    ; ===== ШАГ 1: Сохраняем текущий i-й столбец матрицы в буфер =====
    ; Вычисляем адрес i-го столбца в матрице: matrix + i*8
    mov rax, matrix
    mov rcx, r15
    shl rcx, 3                     ; rcx = i * 8
    add rax, rcx                   ; rax = адрес i-го столбца !!!!!!!!!! просто [matrix + r15 * 8]
    
    push r15                       ; сохраняем i
    push rax                       ; сохраняем адрес i-го столбца
    
    ; Копируем из матрицы (rax) в буфер (r14)
    mov rsi, rax                   ; откуда: i-й столбец в матрице
    mov rdi, r14                   ; куда: буфер
    mov rcx, r13                   ; сколько: rows строк
    call copy_column_to_buffer     ; копируем столбец в буфер (элементы подряд)
    
    pop rax                        ; восстанавливаем адрес i-го столбца
    pop r15                        ; восстанавливаем i
    
    ; ===== ШАГ 2: Копируем столбец из ptr_arr[i] на место i-го столбца =====
    ; Адрес нужного столбца берем из ptr_arr[i]
    mov rbx, [ptr_arr + r15*8]     ; rbx = адрес столбца, который должен быть на позиции i

    ; Находим индекс j, где ptr_arr[j] == matrix + r15*8 (исходный адрес i-го столбца)
    mov rdx, matrix ; начнием от первого элемента в матрице
    mov rcx, r15 ; r15 - сколько столбцов уже стоит на правильном месте, идекс для следующего столбца
    shl rcx, 3 ; переход к следующему числу * 8
    add rdx, rcx                   ; rdx = matrix + i*8
    ; Это адрес, который физически соответствует i-му столбцу в матрице.
    
    xor rcx, rcx
    ; Ищем индекс j, где ptr_arr[j] равен этому адресу:
    ; Мы ищем, какой элемент ptr_arr указывает на физический адрес i-го столбца.
    ; До перестановки:
    ; ptr_arr[i] указывает на столбец, который должен быть на позиции i
    ; Где-то есть индекс j, где ptr_arr[j] указывает на физический i-й столбец
.find_j:
    cmp rcx, r12
    ; jge .error
    cmp [ptr_arr + rcx*8], rdx ; ptr_arr[j] == matrix + i*8 ?
    je .found_j
    inc rcx
    jmp .find_j
    
.found_j:
    ; rcx = j (индекс, откуда берем правильный столбец)
    push r15
    push rax
    push rbx
    push rcx
    
    
    ; Копируем из источника (rbx) в назначение (rax - i-й столбец матрицы)
    mov rsi, rbx                   ; откуда: столбец из ptr_arr[i]
    mov rdi, rax                   ; куда: i-й столбец в матрице
    mov rcx, r13                   ; сколько: rows строк
    call copy_column_to_matrix     ; копируем столбец в матрицу (с шагом shift_next)
    
    pop rcx
    pop rbx
    pop rax
    pop r15
    
    ; ===== ШАГ 3: Копируем из буфера на место, откуда взяли столбец =====
    ; Место назначения: адрес ptr_arr[i] (rbx)
    
    push r15
    push rbx
    push rcx
    
    ; Копируем из буфера (r14) в место, откуда взяли столбец (rbx)
    mov rsi, r14                   ; откуда: буфер
    mov rdi, rbx                   ; куда: адрес ptr_arr[i]
    mov rcx, r13                   ; сколько: rows строк
    call copy_column_to_matrix_from_buf     ; копируем столбец в матрицу
    
    pop rcx
    pop rbx
    pop r15
    

    ; ===== ШАГ 4: ОБНОВЛЯЕМ ptr_arr =====
    ; Меняем местами указатели в ptr_arr[i] и ptr_arr[j]
    push r15
    push rcx
    
    mov rax, [ptr_arr + r15*8]
    mov rdx, [ptr_arr + rcx*8]
    mov [ptr_arr + r15*8], rdx
    mov [ptr_arr + rcx*8], rax
    
    pop rcx
    pop r15

    ; Переходим к следующему столбцу
    inc r15
    jmp .main_loop
    
.done:
    ret

; =============================================
; copy_column_to_buffer - копирует столбец из матрицы в буфер
; Вход: 
;   rsi = адрес начала столбца в матрице
;   rdi = адрес буфера (память, куда копируем ПОДРЯД)
;   rcx = количество строк
; =============================================
copy_column_to_buffer:
    push rsi
    push rax
    
    mov rdx, [shift_next]           ; шаг до следующей строки в матрице
    
.buffer_loop:
    mov rax, [rsi]                  ; читаем элемент из матрицы
    mov [rdi], rax                   ; записываем в буфер (подряд, без смещений)
    add rsi, rdx                     ; следующая строка в матрице
    add rdi, 8                       ; следующая ячейка в буфере
    loop .buffer_loop
    
    pop rax
    pop rsi
    ret

; =============================================
; copy_column_to_matrix - копирует столбец в матрицу
; Вход:
;   rsi = адрес источника (может быть буфер или другой столбец)
;   rdi = адрес назначения в матрице
;   rcx = количество строк
; 
; ВНИМАНИЕ: эта функция предполагает, что источник (rsi) 
;           содержит элементы ПОДРЯД (как в буфере)
; =============================================
copy_column_to_matrix:
    push rax
    
    mov rdx, [shift_next]           ; шаг до следующей строки в матрице
    
.matrix_loop:
    mov rax, [rsi]                  ; читаем элемент из источника (столбец матрицы)
    mov [rdi], rax                   ; записываем в матрицу
    add rsi, rdx                       ; следующий элемент в источнике (столбец матрицы)
    add rdi, rdx                     ; следующая строка в матрице
    loop .matrix_loop
    
    pop rax
    ret


copy_column_to_matrix_from_buf:
    push rax
    
    mov rdx, [shift_next]           ; шаг до следующей строки в матрице
    
.matrix_loop:
    mov rax, [rsi]                  ; читаем элемент из источника (буфера)
    mov [rdi], rax                   ; записываем в матрицу
    add rsi, 8                       ; следующий элемент в источнике (буфер идет подряд)
    add rdi, rdx                     ; следующая строка в матрице
    loop .matrix_loop
    
    pop rax
    ret
